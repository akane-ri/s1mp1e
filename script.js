function inputVisible() {
    document.getElementById('inputSearch').style.display = 'block';
    document.getElementById('inputSearch').focus();
    document.getElementById('exitInput').style.display = 'block';
}
function inputInvisible() {
    document.getElementById('inputSearch').style.display = 'none';
    document.getElementById('exitInput').style.display = 'none';
}
function downMenuVisible() {
    document.getElementById('downMenu').style.display = 'block';
}
function downMenuInVisible() {
    document.getElementById('downMenu').style.display = 'none';
}

